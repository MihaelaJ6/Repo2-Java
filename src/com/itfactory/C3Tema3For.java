package com.itfactory;
public class C3Tema3For {
    /**
     * Sa se afiseze numerele de la 0 la 20 pe aceeasi linie utilizand o bucla for
     * @param args
     */
    public static void main(String[] args){
        for (int x=0; x<=20; x++) {
            System.out.println("Se afiseaza numarul: " + x );
        }
    }
}